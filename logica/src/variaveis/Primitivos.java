package variaveis;

public class Primitivos {
	public static void main(String[] args) {
		/*
		 * Tipos primitivos em ordem hierarquica:
		 * boolean => tipo lógico (true or false)
		 * char => armazena um caracter
		 * byte => menor entre os inteiros (-127 até 127)
		 * short => números inteiros (~ -32000 até 32000)
		 * int => numeros inteiros (padrão)
		 * long => números inteiros
		 * float => menor casas decimais
		 * double => maior casas decimais (padrão)
		 * 
		 * Cast: é uma conversão entre tipos compatíveis, apenas estão em hierarquias diferentes
		 */
		boolean gravida = true;
		char sexo = 'M';
		byte golsdovisitante = 0;
		short qtdecestas = 0;
		long cpf = 0;
		float nota = 0;
		double valorlitrocombustivel = 0;
		float desconto = 500 * (float) 0.9;
		
	}
}
